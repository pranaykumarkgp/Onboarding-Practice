package com.java8.lambdas;

public class Apple {
    String colour;
    int weight;

    public Apple(String colour, int weight) {
        this.colour = colour;
        this.weight = weight;
    }

    public String getColour() {
        return colour;
    }

    public int getWeight() {
        return weight;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
